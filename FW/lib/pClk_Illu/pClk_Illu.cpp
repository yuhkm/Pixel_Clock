#include "pClk_Illu.h"



//--------------------------------------------------------------------------------
//  PClkIllu
//--------------------------------------------------------------------------------

PClkIllu::PClkIllu(int pin) 
    : _nPin(pin)
{

}


PClkIllu::~PClkIllu()
{

}


//--------------------------------------------------------------------------------
//  Init
//--------------------------------------------------------------------------------

void PClkIllu::init()
{
    pinMode(_nPin, INPUT);
    analogSetAttenuation(ADC_11db); 
}


//--------------------------------------------------------------------------------
//  Get ADC value (0~4095)
//--------------------------------------------------------------------------------

int PClkIllu::get()
{
    return analogRead(_nPin);
}
